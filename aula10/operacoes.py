def somar(num1, num2):
    return (num1 + num2)

def subtrair(num1, num2):
    return (num1 - num2)

def multiplicar(num1, num2):
    return (num1 * num2)

def dividir(num1, num2):
    return (num1 / num2)

def dividir_int(num1, num2):
    return (num1 // num2)

def raiz(num1, num2):
    return (num1 ** (1/num2))

def resto_div(num1, num2):
    return (num1 % num2)